package io.github.zhuyajie666.bookmanagesystem.entity.model;

public class BookCount {

    private Integer total;

    private Integer totalInventory;

    private Integer totalRemainInventory;

    public Integer getTotal() {
        return total;
    }

    public void setTotal(Integer total) {
        this.total = total;
    }

    public Integer getTotalInventory() {
        return totalInventory;
    }

    public void setTotalInventory(Integer totalInventory) {
        this.totalInventory = totalInventory;
    }

    public Integer getTotalRemainInventory() {
        return totalRemainInventory;
    }

    public void setTotalRemainInventory(Integer totalRemainInventory) {
        this.totalRemainInventory = totalRemainInventory;
    }
}
